#include <stdio.h>
#include <stdlib.h>

int main(int argc, char const *argv[])
{
    // some spicy number functions
    int negative = -5;
    // abs() - returns absolute value
    printf("%i", abs(negative));
    // ceil() - returns the roundup of a num
    double decimal = 3.1;
    printf("%i", ceil(decimal));


    return 0;
}
